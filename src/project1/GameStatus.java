package project1;

/**
 * enum GameStatus holds the states a SuperTicTacToe game can be in.
 * X_WON: Player X won
 * O_WON: Player O won
 * CATS: Tie between the players
 * IN_PROGRESS: The game is still running / in progress
 */
public enum GameStatus {
    X_WON,
    O_WON,
    CATS,
    IN_PROGRESS
}
